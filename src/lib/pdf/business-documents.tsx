export async function generateKeyValuePdf({
  title,
  subtitle,
  rows
}: {
  title: string;
  subtitle?: string;
  rows: Array<[string, string | number | null | undefined]>;
}) {
  return renderToBuffer(
    <Document>
      <Page size="A4" style={styles.page}>
        <View style={styles.header}>
          <Text style={styles.title}>{title}</Text>
          <Text style={styles.subtitle}>{subtitle ?? "Generated by KIM-ERB"}</Text>
        </View>
        {rows.map(([label, value]) => (
          <View key={label} style={styles.tableRow}>
            <Text style={styles.col}>{label}</Text>
            <Text style={styles.col}>{value == null || value === "" ? "-" : String(value)}</Text>
          </View>
        ))}
        <View style={styles.signature}>
          <Text style={styles.signatureBox}>Prepared by</Text>
          <Text style={styles.signatureBox}>Approved by</Text>
        </View>
        <Text style={styles.footer}>This document is print-ready and can be archived or shared with the customer.</Text>
      </Page>
    </Document>
  );
}
